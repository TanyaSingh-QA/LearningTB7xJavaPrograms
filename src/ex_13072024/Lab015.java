package ex_13072024;

public class Lab015 {
    public static void main(String[] args) {
        // data_type variable_name = variable_value(literal);
        //Data Type
        //Primitive Data Type
        //Boolean
            //true or false
        boolean is_married = true;
        boolean are_you_a_dog = false;
        System.out.println(is_married);
        System.out.println(are_you_a_dog);

        //numerical
            //integral - byte,short,int,long
        byte b = 100;
        System.out.println(b);
        short s = 101;
        System.out.println(s);
        int age = 130;
        System.out.println(age);
        long phone_number = 9876543210L;
        System.out.println(phone_number);

            //char A,B
        char grade ='A'; // O,A,B,C,D,E,F = Fail
        System.out.println(grade);

        //floating
            //float
        float pi = 3.14f;
        System.out.println(pi);
        //double
        double d = 118.654d;
        System.out.println(d);

        //Non-Primitive Data Types
        // String  name -> "tanya" -, number of char or a bunch of chars
        String name = "Tanya";
        String name2= "T";
        System.out.println(name + "and" + name2);




    }
}
